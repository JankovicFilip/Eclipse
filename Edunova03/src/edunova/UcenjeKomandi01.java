package edunova;

public class UcenjeKomandi01 {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			for (int j = 1; j <= 10; j++) {
				System.out.printf("%4d",(i*j)); //"4d" znaci da ce formatirati sa 4 razmaka i izgledati ce pregledno
			}
			System.out.println();

		}

	}

}
